
# pair of parentheses to denote the empty tuple
t1 = ()

# trailing comma for a singleton tuple
t2 = 1,
t3 = (2,)

# separating items with commas
t4 = 1,2,3
t5 = (3,4,5)

# using the tuple()
t6 = tuple()
t7 = tuple([1,2,3,4])

print(t1)
print(t2)
print(t3)
print(t4)
print(t5)
print(t6)
print(t7)


